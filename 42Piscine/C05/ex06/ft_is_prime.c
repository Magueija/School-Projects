int		ft_is_prime(int nb)
{
	int i;

	if (nb < 2)
		return (0);
	else if (nb == 2)
		return (1);
	i = 3;
	while (nb % i != 0 && i <= nb)
		i += 2;
	if (i == nb && nb % i == 0)
		return (1);
	return (0);
}
