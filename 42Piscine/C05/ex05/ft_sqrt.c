int		ft_sqrt(int nb)
{
	long long i;

	if (nb <= 0)
		return (0);
	else if (nb == 1)
		return (1);
	i = 0;
	while ((i * i) < nb)
		i++;
	if (i * i == nb)
		return (i);
	return (0);
}
