void	ft_rev_int_tab(int *tab, int size)
{
	int		i;
	int		sz;
	int		nb;

	sz = size / 2;
	i = 0;
	while (sz > 0)
	{
		nb = tab[size - 1];
		tab[size - 1] = tab[i];
		tab[i] = nb;
		sz--;
		size--;
		i++;
	}
}
