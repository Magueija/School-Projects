#include <unistd.h>

void	ft_print_comb(void)
{
	char num[5];

	num[3] = ',';
	num[4] = ' ';
	num[0] = -1 + '0';
	while (++num[0] <= '7')
	{
		num[1] = num[0];
		while (++num[1] <= '8')
		{
			num[2] = num[1];
			while (++num[2] <= '9')
			{
				if ((num[0] != num[1] && num[0] != num[2] && num[1] != num[2])
						&& (num[0] < num[1] && num[1] < num[2]))
				{
					if (num[0] == '7' && num[1] == '8' && num[2] == '9')
						write(1, &num, 3);
					else
						write(1, &num, 5);
				}
			}
		}
	}
}
